#include "MenuItem.hpp"

MenuItem::MenuItem(std::string name, double price)
{
    this->name = name;
    this->price = price;
}

MenuItem::~MenuItem()
{
    
}

std::string MenuItem::getName()
{
    return name;
}

double MenuItem::getPrice()
{
    return price;
}

void MenuItem::setPrice(double price)
{
    this->price = price;
}


std::string MenuItem::tableName() const
{
    return "MenuItem";
}

std::vector<std::string> MenuItem::columns() const
{
    return { "name", "price" };
}

std::set<std::string> MenuItem::keys() const
{
    return { columns()[0] };
}

bool MenuItem::isAutoGeneratedKey() const
{
    return false;
}

std::map<std::string, std::any> MenuItem::toMap() const
{
    std::map<std::string, std::any> mMap;
    mMap[columns()[0]] = name;
    mMap[columns()[1]] = price;
    
    return mMap;
}

Model * MenuItem::fromMap(std::map<std::string, std::any> mMap) const
{
    std::string mName = std::any_cast<std::string>(mMap[columns()[0]]);
    double mPrice = std::any_cast<double>(mMap[columns()[1]]);
    MenuItem *model = new MenuItem(mName, mPrice);
    return model;
}
